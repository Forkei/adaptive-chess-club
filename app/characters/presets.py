"""Preset character definitions.

These are seeded idempotently on app startup via `preset_key`. Their
memories are generated by the same generator that handles user-created
characters, kicked off in the background after seeding.
"""

from __future__ import annotations

from dataclasses import dataclass, field

from app.models.character import ContentRating


@dataclass(frozen=True)
class PresetSpec:
    preset_key: str
    name: str
    short_description: str
    backstory: str
    avatar_emoji: str
    aggression: int
    risk_tolerance: int
    patience: int
    trash_talk: int
    target_elo: int
    max_elo: int
    adaptive: bool
    opening_preferences: list[str] = field(default_factory=list)
    voice_descriptor: str = ""
    quirks: str = ""
    content_rating: ContentRating = ContentRating.FAMILY


